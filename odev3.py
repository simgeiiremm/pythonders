# Selenium Nedir ?

# Web uygulamaları için test otomasyonu oluşturmaya yarayan açık kaynak bir framework'tur.
# Bir çok dil desteği sunmaktadır ve ücretsizdir. (Python, Java, Ruby)
# Birçok browser destek sunmaktadır (Chrome, Firefox, Explorer)
# Selenium Python'da Eylem Zinciri Yöntemleri
# 1) click() -> Bir öğeyi tıklar
# 2) click_and_hold() -> Bir öğe üzerinde farenin sol düğmesini basılı tutar.
# 3) context_click() -> Bir öğe üzerinde bağlam tıklaması (sağ tıklama) gerçekleştirir.
# 4) double_click() -> Bir öğeyi çift tıklar.
# 5) drag_and_drop() -> Kaynak öğe üzerinde farenin sol düğmesini basılı tutar, ardından hedef öğeye gider ve fare düğmesini bırakır.
# 6) key_down() -> Serbest bırakmadan yalnızca bir tuşa basma gönderir.
# 7) key_up() -> Bir değiştirici tuşu serbest bırakır.
# 8) move_to_element() -> Fareyi bir öğenin ortasına hareket ettirmek.
# 9) pause() -> Tüm girişleri saniye cinsinden belirtilen süre boyunca duraklat
# 10) send_keys() -> Anahtarları geçerli odaklanılan öğeye gönderir.






