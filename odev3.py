# HTML Locators nedir?

# Konumlandırıcılar , bir web sayfasındaki bir HTML öğesini tanımlamanın yoludur ve neredeyse tüm UI otomasyon araçları, bir web sayfasındaki HTML öğelerinin tanımlanması için konumlandırıcıları kullanma yeteneği sağlar. Aynı eğilimi takip eden Selenium, HTML öğelerinin tanımlanması için "Yer Belirleyiciler" kullanma yeteneğine de sahiptir ve popüler olarak "Selenyum Yer Belirleyiciler" olarak bilinir. Selenyum, çeşitli yer belirleyici türlerini destekler.

# Selenium Yer Belirleyiciler, bir web sayfasındaki doğru GUI öğesinin tanımlanması, herhangi bir başarılı otomasyon komut dosyası oluşturmak için ön koşuldur. Yer bulucuların resme girdiği yer burasıdır. Yer belirleyiciler Selenium altyapısının temel bileşenlerinden biridir ve Selenium web sayfasında bulunan WebElements'i (metin kutusu, düğme vb.) benzersiz bir şekilde tanımlamasına yardımcı olur.


